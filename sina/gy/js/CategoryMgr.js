	this.show = function(offset){
		if(!offset){
			$("#left").html(this.getCategoryHtmlLeft());
			$("#right").html(this.getCategoryHtmlRight(offset));
			offset = 0;
			this.selByOffset(0);
		}

		this.setCurItemIndex(offset);
		if(0 != offset)
			$("#right").animate({scrollTop:offset * 77},200);
	};
	
	this.selByOffset = function(offset){
		if(0 == offset)
			$("[id^=left_li_]").first().addClass("li_sel");
		else
			$("[id^=left_li_]").eq(this.items.get(offset).getCategory().getIndex()).addClass("li_sel");
	};
	
