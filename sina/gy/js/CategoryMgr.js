	this.toJsonObject = function(){
		if(this.countSel < 1)
			return null;
		var sb = new StringBuilder();
		sb.append("{");
		sb.append("'count':").append(this.countSel).append(",");
		sb.append("'price':").append(this.calcAmount()).append(",");
		
		sb.append("'data':[");
		var b = 0;
		for(var i = 0, j = this.categorys.size(); i < j; i ++){
			var ti = this.categorys.get(i);
			if(ti.getCount() < 1)
				continue;
			b = 1;
			sb.append("{'name':").append("'").append(ti.getName()).append("',").append("data:[");
			var items = ti.getItems();
			for(var k = 0, m = items.size(); k < m; k ++){
				var dk = items.get(k);
				if(dk && dk.getCount() > 0){
					var dkcount = dk.getCount();
					var dkprice = dk.getPrice();
					sb.append("{'name':'").append(dk.getName()).append("',");
					sb.append("'spec':'").append(dk.getSpec()).append("',");
					sb.append("'price':'").append(dkprice).append(dk.getPriceTail()).append("',");
					try{
						sb.append("'amount':'").append((parseFloat(dkprice) * parseInt(dkcount)).toFixed(2)).append("',");
					}catch(E){
						
					}
					sb.append("'count':").append(dkcount).append("},");
				}
			}
			if(sb.endWith(","))
				sb.deleteCharAtLast();
			sb.append("]},");
		}
		
		if(sb.endWith(","))
			sb.deleteCharAtLast();
		
		sb.append("]}");
		return sb.toString();
	};
	
