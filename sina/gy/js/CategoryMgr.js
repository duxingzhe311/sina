	this.findItemById = function(cid,id){
		if(!cid)
			return null;
		if(!id)
			return null;
		var key = cid + "_" + id;
		var c = this.cache.get(key);
		if(c){
			return c;
		}
		
		if(!this.categorys || this.categorys.isEmpty())
			return null;
		for(var i = 0, j = this.categorys.size(); i < j; i ++){
			var ti = this.categorys.get(i);
			if(cid != ti.getId())
				continue;
			var res = ti.findItemById(id);
			if(res){
				this.cache.put(key,res);
				return res;
			}
		}
		return null;
	};
	this.findCagegoryByName = function(name){
		if(!name)
			return null;
		if(!this.categorys || this.categorys.isEmpty())
			return null;
		for(var i = 0, j = this.categorys.size(); i < j; i ++){
			var ti = this.categorys.get(i);
			if(name == ti.name)
				return ti;
		}
		return null;
	};
	
	this.calcAmount = function(){
		if(!this.categorys || this.categorys.isEmpty())
			return 0;
		var res = 0;
		for(var i = 0, j = this.categorys.size(); i < j; i++){
			var ti = this.categorys.get(i);
			var tiItems = ti.getItems();
			if(tiItems && !tiItems.isEmpty()){
				for(var k = 0, m = tiItems.size(); k < m; k++){
					var tk = tiItems.get(k);
					var n = 0;
					if(tk && (n = tk.getCount()) > 0)
						res += n * tk.getPrice();
				}
			}
		}
		return res.toFixed(2);
	};
	
